// Use the typeof operator to check the type of a variable.

// Declaring variable
let name = "Israel";         // String
let age = 28;              // Number
let isStudent = true;      // Boolean
let undefinedVar;          // Undefined
let obj = { key: "value" }; // object

// output to cross-check types of declared variable
console.log("Type of name:", typeof name);       
console.log("Type of age:", typeof age);          
console.log("Type of isStudent:", typeof isStudent); 
console.log("Type of undefinedVar:", typeof undefinedVar); 
console.log("Type of obj:", typeof obj);           

// Compare two values using the equality (==) and strict equality (===) operators.
console.log(28 == "28");     
console.log(false === 0); 

