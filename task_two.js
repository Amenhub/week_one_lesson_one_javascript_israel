//Creating variables for numeric value
let number1 = 25;
let number2 = 6;

// Performing addition of the two numbers
let addition = number1 + number2;
console.log("Addition = " + addition); 

// Performing subtraction betweenthe two numbers
let subtraction = number1 - number2;
console.log("Subtraction = " + subtraction);

// Performing multiplication of the two numbers
let multiplication = number1 * number2;
console.log("Multiplication = " + multiplication);

// Performing division on the two numbers
let division = number1 / number2;
console.log("Division = " + division);

// using modulus operator of the two numbers
let modulus = number1 % number2;
console.log("Modulus (Remainder of number1 / number2): " + modulus);